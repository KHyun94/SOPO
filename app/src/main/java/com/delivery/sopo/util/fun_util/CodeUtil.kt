            VALIDATION_ERROR.CODE -> VALIDATION_ERROR.MSG
            KAKAO_SIMPLE_FAIL_TO_UPDATE_AUTH_TOKEN.CODE -> KAKAO_SIMPLE_FAIL_TO_UPDATE_AUTH_TOKEN.MSG
