            SEARCH_PARCEL_NULL_ERROR.CODE -> SEARCH_PARCEL_NULL_ERROR.MSG
            FAIL_TO_SAVE_PARCEL.CODE -> FAIL_TO_SAVE_PARCEL.MSG
